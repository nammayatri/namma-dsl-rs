fn emit_spec_query(out: &mut String, q: &QueryDef, table: &TableDef, enum_names: &[String]) {
    let model_name = format!("{}Model", table.table_name_rust);
    let domain_name = &table.table_name_rust;
    let sql_table = &table.table_name_sql;

    match q.kv_function {
        KvFunction::FindOne | KvFunction::FindOnePartial => {
            let params = where_clause_params(&q.where_clause, table);
            let params_str = if params.is_empty() {
                String::new()
            } else {
                format!(", {}", params.join(", "))
            };
            writeln!(
                out,
                "pub async fn {}(pool: &DbPool{}) -> Result<Option<{}>, DbError> {{",
                q.name_snake, params_str, domain_name
            )
            .unwrap();
            writeln!(out, "    let mut conn = pool.get().await?;").unwrap();
            writeln!(out, "    {}::table", sql_table).unwrap();
            emit_where_filters(out, &q.where_clause, sql_table, table);
            if let Some(ref ob) = q.order_by {
                let dir = match ob.direction {
                    OrderDirection::Asc => "asc",
                    OrderDirection::Desc => "desc",
                };
                writeln!(
                    out,
                    "        .order({}::{}.{}())",
                    sql_table,
                    utils::to_snake(&ob.field),
                    dir
                )
                .unwrap();
            }
            writeln!(out, "        .first::<{}>(&mut *conn)", model_name).unwrap();
            writeln!(out, "        .await").unwrap();
            writeln!(out, "        .optional()").unwrap();
            writeln!(out, "        .map(|opt| opt.map({}::from))", domain_name).unwrap();
            writeln!(out, "        .map_err(DbError::from)").unwrap();
            writeln!(out, "}}").unwrap();
            writeln!(out).unwrap();
        }
        KvFunction::FindAll | KvFunction::FindAllPartial => {
            let params = where_clause_params(&q.where_clause, table);
            let params_str = if params.is_empty() {
                String::new()
            } else {
                format!(", {}", params.join(", "))
            };
            writeln!(
                out,
                "pub async fn {}(pool: &DbPool{}) -> Result<Vec<{}>, DbError> {{",
                q.name_snake, params_str, domain_name
            )
            .unwrap();
            writeln!(out, "    let mut conn = pool.get().await?;").unwrap();
            writeln!(out, "    {}::table", sql_table).unwrap();
            emit_where_filters(out, &q.where_clause, sql_table, table);
            if let Some(ref ob) = q.order_by {
                let dir = match ob.direction {
                    OrderDirection::Asc => "asc",
                    OrderDirection::Desc => "desc",
                };
                writeln!(
                    out,
                    "        .order({}::{}.{}())",
                    sql_table,
                    utils::to_snake(&ob.field),
                    dir
                )
                .unwrap();
            }
            writeln!(out, "        .load::<{}>(&mut *conn)", model_name).unwrap();
            writeln!(out, "        .await").unwrap();
            writeln!(
                out,
                "        .map(|rows| rows.into_iter().map({}::from).collect())",
                domain_name
            )
            .unwrap();
            writeln!(out, "        .map_err(DbError::from)").unwrap();
            writeln!(out, "}}").unwrap();
            writeln!(out).unwrap();
        }
        KvFunction::Update | KvFunction::UpdateOne => {
            let where_params = where_clause_params(&q.where_clause, table);
            let set_params: Vec<String> = q
                .params
                .iter()
                .filter_map(|p| {
                    table
                        .fields
                        .iter()
                        .find(|f| f.field_name == *p)
                        .map(format_param)
                })
                .collect();
            let all_params: Vec<String> = set_params
                .iter()
                .chain(where_params.iter())
                .cloned()
                .collect();
            let params_str = if all_params.is_empty() {
                String::new()
            } else {
                format!(", {}", all_params.join(", "))
            };
            writeln!(
                out,
                "pub async fn {}(pool: &DbPool{}) -> Result<(), DbError> {{",
                q.name_snake, params_str
            )
            .unwrap();
            writeln!(out, "    let mut conn = pool.get().await?;").unwrap();
            writeln!(out, "    diesel::update({}::table", sql_table).unwrap();
            emit_where_filters(out, &q.where_clause, sql_table, table);
            writeln!(out, "    )").unwrap();
            writeln!(out, "    .set((").unwrap();
            for p in &q.params {
                let snake = utils::to_snake(p);
                if let Some(f) = table.fields.iter().find(|f| f.field_name == *p) {
                    let val = param_value_for_set(f, enum_names);
                    writeln!(out, "        {}::{}.eq({}),", sql_table, snake, val).unwrap();
                }
            }
            writeln!(out, "    ))").unwrap();
            writeln!(out, "    .execute(&mut *conn)").unwrap();
            writeln!(out, "    .await").unwrap();
            writeln!(out, "    .map_err(DbError::from)?;").unwrap();
            writeln!(out, "    Ok(())").unwrap();
            writeln!(out, "}}").unwrap();
            writeln!(out).unwrap();
        }
        KvFunction::Delete => {
            let params = where_clause_params(&q.where_clause, table);
            let params_str = if params.is_empty() {
                String::new()
            } else {
                format!(", {}", params.join(", "))
            };
            writeln!(
                out,
                "pub async fn {}(pool: &DbPool{}) -> Result<(), DbError> {{",
                q.name_snake, params_str
            )
            .unwrap();
            writeln!(out, "    let mut conn = pool.get().await?;").unwrap();
            writeln!(out, "    diesel::delete({}::table", sql_table).unwrap();
            emit_where_filters(out, &q.where_clause, sql_table, table);
            writeln!(out, "    )").unwrap();
            writeln!(out, "    .execute(&mut *conn)").unwrap();
            writeln!(out, "    .await").unwrap();
            writeln!(out, "    .map_err(DbError::from)?;").unwrap();
            writeln!(out, "    Ok(())").unwrap();
            writeln!(out, "}}").unwrap();
            writeln!(out).unwrap();
        }
    }
    let _ = (model_name, delete_with_kv_stub());
}

fn delete_with_kv_stub() -> &'static str {
    "" // referenced from import block; emission of delete_with_kv via spec queries is future scope
}

fn emit_from_model_impl(out: &mut String, table: &TableDef, enum_names: &[String]) {
    let model_name = format!("{}Model", table.table_name_rust);
    let domain_name = &table.table_name_rust;

    writeln!(out, "impl From<{}> for {} {{", model_name, domain_name).unwrap();
    writeln!(out, "    fn from(model: {}) -> Self {{", model_name).unwrap();
    writeln!(out, "        {} {{", domain_name).unwrap();
    for f in &table.fields {
        let conversion = model_field_to_domain(f, enum_names);
        writeln!(out, "            {}: {},", f.field_name_snake, conversion).unwrap();
    }
    writeln!(out, "        }}").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();
}

fn emit_to_model_impl(out: &mut String, table: &TableDef, enum_names: &[String]) {
    let model_name = format!("{}Model", table.table_name_rust);
    let domain_name = &table.table_name_rust;

    writeln!(out, "impl From<&{}> for {} {{", domain_name, model_name).unwrap();
    writeln!(out, "    fn from(domain: &{}) -> Self {{", domain_name).unwrap();
    writeln!(out, "        {} {{", model_name).unwrap();
    for f in &table.fields {
        let conversion = domain_field_to_model(f, enum_names);
        writeln!(out, "            {}: {},", f.field_name_snake, conversion).unwrap();
    }
    writeln!(out, "        }}").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();
}

fn format_param(f: &FieldDef) -> String {
    if f.rust_type.starts_with("Option<") {
        let inner = utils::unwrap_option(&f.rust_type);
        if utils::is_id_type(inner) {
            let id_inner = utils::extract_id_inner(inner);
            format!("{}: Option<&Id<{}>>", f.field_name_snake, id_inner)
        } else {
            format!("{}: Option<&{}>", f.field_name_snake, inner)
        }
    } else if utils::is_id_type(&f.rust_type) {
        let id_inner = utils::extract_id_inner(&f.rust_type);
        format!("{}: &Id<{}>", f.field_name_snake, id_inner)
    } else if f.rust_type == "String" {
        format!("{}: &str", f.field_name_snake)
    } else if utils::is_vec_type(&f.rust_type) {
        format!("{}: &{}", f.field_name_snake, f.rust_type)
    } else {
        format!("{}: {}", f.field_name_snake, f.rust_type)
    }
}

fn param_to_filter_value(f: &FieldDef, enum_names: &[String]) -> String {
    let inner = utils::unwrap_option(&f.rust_type);
    if utils::is_id_type(&f.rust_type) {
        format!("{}.inner()", f.field_name_snake)
    } else if enum_names.contains(&inner.to_string()) {
        format!("{}.to_string()", f.field_name_snake)
    } else {
        f.field_name_snake.clone()
    }
}

fn entity_field_to_db_value(f: &FieldDef, enum_names: &[String]) -> String {
    let snake = &f.field_name_snake;
    let inner_type = utils::unwrap_option(&f.rust_type);

    if f.is_nullable && utils::is_id_type(inner_type) {
        format!("entity.{}.as_ref().map(|id| id.inner().to_string())", snake)
    } else if utils::is_id_type(inner_type) {
        format!("entity.{}.inner().to_string()", snake)
    } else if f.is_nullable && enum_names.contains(&inner_type.to_string()) {
        format!("entity.{}.as_ref().map(|e| e.to_string())", snake)
    } else if enum_names.contains(&inner_type.to_string()) {
        format!("entity.{}.to_string()", snake)
    } else if inner_type == "chrono::DateTime<chrono::Utc>" {
        if f.is_nullable {
            format!("entity.{}.map(|dt| dt.naive_utc())", snake)
        } else {
            format!("entity.{}.naive_utc()", snake)
        }
    } else if f.rust_type == "String" {
        format!("entity.{}.clone()", snake)
    } else if f.is_nullable && f.rust_type.contains("String") {
        format!("entity.{}.clone()", snake)
    } else {
        format!("entity.{}", snake)
    }
}

