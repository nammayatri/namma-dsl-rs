fn where_clause_params(wc: &WhereClause, table: &TableDef) -> Vec<String> {
    match wc {
        WhereClause::Empty => vec![],
        WhereClause::Leaf(field_name) => {
            if let Some(f) = table.fields.iter().find(|f| f.field_name == *field_name) {
                vec![format_param(f)]
            } else {
                vec![format!("{}: &str", utils::to_snake(field_name))]
            }
        }
        WhereClause::And(clauses) | WhereClause::Or(clauses) => clauses
            .iter()
            .flat_map(|c| where_clause_params(c, table))
            .collect(),
        WhereClause::In(fields) => fields
            .iter()
            .filter_map(|field_name| {
                table
                    .fields
                    .iter()
                    .find(|f| f.field_name == *field_name)
                    .map(|f| {
                        let inner = utils::unwrap_option(&f.rust_type);
                        if utils::is_id_type(inner) {
                            let id_inner = utils::extract_id_inner(inner);
                            format!("{}: &[Id<{}>]", f.field_name_snake, id_inner)
                        } else {
                            format!("{}: &[{}]", f.field_name_snake, inner)
                        }
                    })
            })
            .collect(),
    }
}

fn emit_where_filters(out: &mut String, wc: &WhereClause, sql_table: &str, table: &TableDef) {
    let enum_names: Vec<String> = table.types.iter().map(|t| t.name.clone()).collect();
    match wc {
        WhereClause::Empty => {}
        WhereClause::Leaf(field_name) => {
            let snake = utils::to_snake(field_name);
            let f = table.fields.iter().find(|f| f.field_name == *field_name);
            let val = if let Some(f) = f {
                param_to_filter_value(f, &enum_names)
            } else {
                snake.clone()
            };
            writeln!(out, "        .filter({}::{}.eq({}))", sql_table, snake, val).unwrap();
        }
        WhereClause::And(clauses) => {
            for c in clauses {
                emit_where_filters(out, c, sql_table, table);
            }
        }
        WhereClause::Or(_) => {} // OR fallback to raw SQL is future scope
        WhereClause::In(fields) => {
            for field_name in fields {
                let snake = utils::to_snake(field_name);
                let f = table.fields.iter().find(|f| f.field_name == *field_name);
                let val = if let Some(f) = f {
                    if utils::is_id_type(&f.rust_type) {
                        format!(
                            "{}.iter().map(|id| id.inner().to_string()).collect::<Vec<_>>()",
                            snake
                        )
                    } else {
                        snake.clone()
                    }
                } else {
                    snake.clone()
                };
                writeln!(
                    out,
                    "        .filter({}::{}.eq_any({}))",
                    sql_table, snake, val
                )
                .unwrap();
            }
        }
    }
}
